static const char * SkipFormattingOptions(const char * p);
static void PrintUnknown(int n);

static const char * SkipFormattingOptions(const char * p)
{
    while (*p && (((*p >= '0') && (*p <= '9')) || (*p == '.') || (*p == ' '))) {
        p++;
    }
    return p;
}

static void PrintUnknown(int n)
{
    (void)n;
    UartTx_PrintString("??");
}

/* -------------------------------------------------------------------------------- */

void UartTx_Init(void)
{
    Chip_IOCON_SetPinConfig(NSS_IOCON, 9, IOCON_FUNC_0 | PULL);
    Chip_SSP_Init(NSS_SSP0);
    Chip_SSP_SetFormat(NSS_SSP0, BITS_PER_SSP_FRAME, SSP_FRAME_FORMAT_TI, SSP_CLOCK_CPHA0_CPOL0);
    Chip_SSP_SetBitRate(NSS_SSP0, UARTTX_BITRATE);
    Chip_SSP_Enable(NSS_SSP0);

#if !UARTTX_INVERTED
    /**
     * In SSP-TI mode and with the SSP block disabled (CR1->SSE bit still at 0), the fifo (serializer) output is kept
     * at '0'. As the SSP mode has to be set before the SSP block is enabled, PIO_9 will always go to '0', if the SSP
     * function for PIO0_9 is chosen before the SSP block is enabled. Hence all '1's (0xFF) are shifted out while
     * PIO0_9 is still configured as GPIO. Once the SSP block is enabled and the dummy all-1s word is shifted out,
     * the output of the serializer will be '1' and the PIO0_9 function can be switched over to SSP mode.
     */
    uint8_t d[] = {0xFF, 0xFF};
    Chip_SSP_WriteFrames_Blocking(NSS_SSP0, d, 2);
#endif

    Chip_IOCON_SetPinConfig(NSS_IOCON, 9, IOCON_FUNC_1 | PULL); /* Configure as MOSI. */
}

void UartTx_DeInit(void)
{
    Chip_IOCON_SetPinConfig(NSS_IOCON, 9, IOCON_FUNC_0 | BOARD_PIO9_PULL);
    Chip_SSP_DeInit(NSS_SSP0);
}

void UartTx_Tx(const uint8_t * pData, unsigned int length)
{
    uint16_t buffer[LEN];
    unsigned int i = 0;

    while (i < length) {
        int j = 0;
        do {
#if UARTTX_INVERTED
            buffer[j] = (uint16_t)~sRbit[pData[i]];
#else
            buffer[j] = sRbit[pData[i]];
#endif
            i++;
            j++;
        } while ((i < length) && (j < LEN));
        Chip_SSP_WriteFrames_Blocking(NSS_SSP0, (uint8_t *)buffer, (uint32_t)j * sizeof(uint16_t));
    }

    while (!Chip_SSP_GetStatus(NSS_SSP0, SSP_STAT_TFE)) {
        ; /* Blocking until the Tx FIFO is empty, to ensure all data has been sent. */
    }
}

